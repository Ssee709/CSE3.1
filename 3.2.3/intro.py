# Basic "FOR" loop

# Looping for a specific list
num_list = [42, 6, 11, 14, 7, 89, 17]

for numbers in num_list:
    print(numbers - 13)

# Looping for specified number of times
for battered in range(14):
    print("Sausage")

# How to use step in range functions
test_list = range(2, 14, 2)
print(test_list)